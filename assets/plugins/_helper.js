const BlackAlpha = require('blackalpha-public');
const ALPHA = BlackAlpha.events
const _alpha = BlackAlpha.tima

const Language = require('../language');
const Lang = Language.getString('helper');

Alpha.operate({ pattern: 'helper ?(.*)', fromMe: true, desc: Lang.DESC, deleteCommand: false, dontAddCommandList: true}, (async (alphaMSG, input) => {
    await BlackAlpha.alpha_setup()
    const help_CMD = input[1]
    await _alpha.helper( alphaMSG, help_CMD, Lang )
}));
